#include<iostream>
using namespace std;
int main()
{
	int i;
	cout<<"\n Enter the value of i : ";
	cin>>i;
	
	i++;//post Increment
	cout<<"\n value of i in post increment : "<<i;
	
	++i;//pre Increment
	cout<<"\n value of i in pre increment : "<<i;
	
	i--;//post Decrement
	cout<<"\n value of i in post Decrement : "<<i;
	
	--i;//pre decrement
	cout<<"\n value of i in pre decrement : "<<i;
	
	return 0;
}
